# Menginput jari-jari Lingkaran
jari = float(input('Tulis Jari-jari Lingkaran: '))

# Hitung Luas dan Keliling Lingkaran
luas = 3.14 * jari * jari
keliling = 3.14 * 2 * jari

#Menampilkan Hasil Perhitungan
print('Luas Lingkaran adalah %0.2f' %luas)
print('keliling Lingkaran adalah %0.2f' %keliling)
